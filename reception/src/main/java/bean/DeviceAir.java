package bean;

public class DeviceAir {
	private String deviceID;
	private int insideAir;
	private int outsideAir;
	private String date;
	public String getDeviceID() {
		return deviceID;
	}
	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}
	public int getInsideAir() {
		return insideAir;
	}
	public void setInsideAir(int insideAir) {
		this.insideAir = insideAir;
	}
	public int getOutsideAir() {
		return outsideAir;
	}
	public void setOutsideAir(int outsideAir) {
		this.outsideAir = outsideAir;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
